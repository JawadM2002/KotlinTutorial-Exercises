package KotlinBasicsTutorials

fun main(args: Array<String>) {

    // number data types
    // 1. Byte
    val range: Byte = 21 // variables are spelled val
    println("Byte: " + "$range")

    // 2. Short
    val temperature: Short = -11245
    println("Short: " + "$temperature")

    // 3. Int
    val score: Int = 100000
    println("Int: " + "$score")

    val score2 = 10
    println("$score2")

    // 4. Long
    val highestScore: Long = 9999
    println("Long: " + "$highestScore")

    // 5. Double
    val distance = 999.5
    println("Double: " + "$distance")

    // Booleans
    val tags = true
    println("$tags")

    // Char
    val brand: Char
    brand = 't'
    println("$brand")

}